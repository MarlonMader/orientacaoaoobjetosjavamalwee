package br.com.senai.pessoa;

public class Pais {

	private String NomeDoPais;
	private String siglaDoPais;
	
	public String getNomeDaCidade() {
		return NomeDoPais;
	}
	public void setNomeDaCidade(String nomeDaCidade) {
		this.NomeDoPais = nomeDaCidade;
	}
	public String getSiglaDoPais() {
		return siglaDoPais;
	}
	public void setSiglaDoPais(String siglaDoPais) {
		this.siglaDoPais = siglaDoPais;
	}
	
}
